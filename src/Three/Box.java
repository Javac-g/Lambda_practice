package Three;

public class Box {
    int weight;
    int height;
    int length;

    Box(int weight,int height, int length){
        this.weight = weight;
        this.height = height;
        this.length = length;
    }






    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }
}
