package L2.Constr;

public class Box {

    int w,h,l;

    Box(int w, int h, int l){

        this.w = w;
        this.h = h;
        this.l = l;

    }

    public int getW() {

        return w;
    }

    public int getH() {

        return h;
    }

    public int getL() {

        return l;
    }
}
