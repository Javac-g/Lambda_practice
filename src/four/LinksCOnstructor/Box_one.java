package four.LinksCOnstructor;

public class Box_one {

    int w,h,l;

    Box_one(int w,int h,int l){
        this.h = h;
        this.w = w;
        this.l = l;
    }

    public int getW() {
        return w;
    }

    public void setW(int w) {
        this.w = w;
    }

    public int getH() {
        return h;
    }

    public void setH(int h) {
        this.h = h;
    }

    public int getL() {
        return l;
    }

    public void setL(int l) {
        this.l = l;
    }
}
