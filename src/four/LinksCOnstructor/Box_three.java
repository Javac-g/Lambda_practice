package four.LinksCOnstructor;

public class Box_three {

    String w,h,l;

    Box_three(String w,String h, String l){
        this.w = w;
        this.h = h;
        this.l = l;
    }

    public String getW() {
        return w;
    }

    public void setW(String w) {
        this.w = w;
    }

    public String getH() {
        return h;
    }

    public void setH(String h) {
        this.h = h;
    }

    public String getL() {
        return l;
    }

    public void setL(String l) {
        this.l = l;
    }
}
