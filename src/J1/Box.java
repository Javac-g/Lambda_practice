package J1;

public class Box {
    Integer w,h,l;

    public Box(Integer w, Integer h, Integer l) {
        this.w = w;
        this.h = h;
        this.l = l;
    }

    public Integer getW() {
        return w;
    }

    public void setW(Integer w) {
        this.w = w;
    }

    public Integer getH() {
        return h;
    }

    public void setH(Integer h) {
        this.h = h;
    }

    public Integer getL() {
        return l;
    }

    public void setL(Integer l) {
        this.l = l;
    }
}
