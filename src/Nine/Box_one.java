package Nine;

public class Box_one {

    int w,h,l;

    Box_one(int w, int h, int l){

        this.w = w;
        this.h = h;
        this.l = l;

    }

    public int getW() {
        return w;
    }

    public int getH() {
        return h;
    }

    public int getL() {
        return l;
    }
}
